const FORM_READINESS_LEVELS = [
  { level: 0, label: 'Source catalog only', meaning: 'Official source is tracked, but Smarter Justice should not guide or complete this path yet.' },
  { level: 1, label: 'Worksheet only', meaning: 'Smarter Justice can collect facts and create an organized starter file for review.' },
  { level: 2, label: 'Field-mapped draft foundation', meaning: 'A future version may map answers to fields after verification; human review remains required.' },
  { level: 3, label: 'Review-ready completed draft', meaning: 'Completed official drafts may be generated only after internal review and verified mapping.' },
  { level: 4, label: 'Sign-ready after review', meaning: 'User-facing completed forms can be delivered only after review, user verification, and payment where applicable.' },
  { level: 5, label: 'Filing support where allowed', meaning: 'Future filing/e-filing support only where permitted and technically/compliance ready.' }
];
function readinessForForm(form, row){
  const text = `${form.automation || ''} ${row.catalogStatus || ''} ${row.sourceName || ''}`.toLowerCase();
  if (/jurisdiction source required|state\/agency source required|official source required/.test(text)) return 0;
  if (/professional engagement required|attorney review|tax professional|tax attorney|patent attorney|ip attorney|review recommended|professional review/.test(text)) return 1;
  if (/worksheet|starter-file/.test(text)) return 1;
  if (/future verified|field/.test(text)) return 2;
  if (/review-ready completed/.test(text)) return 3;
  if (/sign-ready/.test(text)) return 4;
  return 1;
}
function decorateCatalogRow(row){
  const forms = (row.forms || []).map(form => {
    const readinessLevel = readinessForForm(form, row);
    return { ...form, readinessLevel, readinessLabel: FORM_READINESS_LEVELS.find(x => x.level === readinessLevel).label };
  });
  const readinessLevel = forms.length ? Math.min(...forms.map(f => f.readinessLevel)) : 0;
  return {
    ...row,
    forms,
    readinessLevel,
    readinessLabel: FORM_READINESS_LEVELS.find(x => x.level === readinessLevel).label,
    completedFormAutomationClaim: false,
    reviewBeforeDeliveryRequired: true,
    sourceVerificationNote: row.officialUrl && !/JURISDICTION_SPECIFIC|STATE_OR_AGENCY/.test(row.officialUrl) ? 'Seeded official source; verify current version/date before form delivery.' : 'Jurisdiction-specific official source must be selected and verified before form guidance.'
  };
}
function listCatalog(){return OFFICIAL_SOURCE_CATALOG.map(decorateCatalogRow);}
function getCatalogForPractice(slug){return OFFICIAL_SOURCE_CATALOG.filter(row=>row.practiceSlug===slug).map(decorateCatalogRow);}
module.exports={OFFICIAL_SOURCE_CATALOG,FORM_READINESS_LEVELS,decorateCatalogRow,listCatalog,getCatalogForPractice};
